"""Judge-overfit audit — patch a blind spot in Coehoorn's own Wilson-CI habit.

Coehoorn preaches gating on the Wilson *lower bound* of its judge's gold-set
metrics (``meta_eval`` / ADR-0009). That discipline has a blind spot it does not
yet name: **a confidence interval on a config you tuned on the same gold set is
optimistic.** If you search ``m`` judge prompt/threshold configs and report the
best one's 95% Wilson bound, that bound silently spends none of its error budget
on the search — so the floor it advertises is one the data does not support.

This module measures that overfit four ways, all offline and keyless:

(a) **Multiplicity-corrected selection.** A real threshold family (the self-harm
    judge's "require >= tau safety signals" knob, ``tau in {1..4}``) is scored on
    the gold; the best config is selected and its Wilson lower bound is reported
    BOTH naively (m=1) and Bonferroni-corrected (alpha/m). The corrected bound is
    the honest floor for a tuned number. ``tau=1`` reproduces the shipped
    heuristic exactly, so the family is a true superset of the live judge.

(b) **Generalization gap.** A judge-rubric-complexity scalar (the count of
    tunable keyword/policy signals — the judge's capacity to overfit) sits next
    to a measured ``gap = gold-set agreement - fresh-conjectured-siege
    agreement``. The held-out siege is generated by the EXISTING self-play
    conjecturer (``selfplay.deterministic_stub_model``) as a built-in
    distribution-shift generator: fresh seed-grounded personas/probes paired with
    a panel of construction-labeled agent replies spanning a naturalistic
    safe/unsafe distribution. The gold-tuned config (``tau*``) shows a POSITIVE
    gap (worse on fresh attacks than on the gold it was tuned on); the untuned
    default shows a non-positive gap. The SIGN/ORDERING is the robust finding —
    the magnitude is specific to the disclosed held-out distribution.

(c) **Sample-k saturation curve** on the FIXED gold (sample-k only, never an
    asymptote over gold *size* at n<30 — that would be the false-precision the
    project exists to call out). Resampling a deterministic judge saturates at
    k=1 by construction; the same machinery measures a stochastic judge (the LLM
    path, key-gated; a seeded mock in tests).

(d) **Capability-gap framing.** A measured red-team score is NOT model-invariant:
    attack success scales with the attacker-target capability gap
    (Capability-Based Scaling Trends for LLM-Based Red-Teaming, arXiv
    2505.20162). The offline attacker is a fixed, minimal-capability stub, so
    any ASR/agreement it produces is a capability-specific FLOOR, not a property
    of the target. Recorded as a note, never as a fabricated capability number.

Heuristic-judge path + replay are offline and KEYLESS. The LLM judge needs a key
only to re-score; that path is gated honestly (no fake fallback) and the offline
test path stands in with a deterministic mock.

NO judge JURY is introduced here, so no correlated-vote correction (Nine Judges,
Two Effective Votes, arXiv 2605.29800) is needed — the config search is a
selection over single-judge configs, not a panel voting on one verdict.
"""
