# (a) Multiplicity-corrected selection.
# --------------------------------------------------------------------------- #
class ConfigScore(BaseModel):
    """One config's gold-set scorecard (the search row)."""

    model_config = ConfigDict(extra="forbid")

    name: str
    tau: int
    complexity: int
    tp: int = Field(ge=0)
    fp: int = Field(ge=0)
    fn: int = Field(ge=0)
    tn: int = Field(ge=0)
    recall: float | None = Field(default=None, ge=0, le=1)
    precision: float | None = Field(default=None, ge=0, le=1)
    balanced_accuracy: float | None = Field(default=None, ge=0, le=1)


class CorrectedBound(BaseModel):
    """A proportion's naive (m=1) vs multiplicity-corrected (alpha/m) Wilson lower
    bound. ``delta`` is how much the search shaved off the advertised floor."""

    model_config = ConfigDict(extra="forbid")

    metric: str
    numerator: int = Field(ge=0)
    denominator: int = Field(ge=0)
    point: float | None = Field(default=None, ge=0, le=1)
    naive_lower: float = Field(ge=0, le=1)
    corrected_lower: float = Field(ge=0, le=1)
    delta: float = Field(ge=0)

    @model_validator(mode="after")
    def _ordered(self) -> CorrectedBound:
        if self.corrected_lower > self.naive_lower + 1e-9:
            raise ValueError("corrected lower bound cannot exceed the naive one")
        return self


class ConfigSelection(BaseModel):
    """The selection audit: ``n_configs`` searched, the selected winner, and the
    multiplicity-corrected Wilson lower bounds for the SELECTED config.

    ``default_*`` are the untuned ``tau=1`` config's honest (m=1) bounds — the
    number you would report if you had NOT searched — for contrast. The selected
    config's ``naive_lower`` is the optimistic mistake; ``corrected_lower`` is the
    honest floor that pays for the search.
    """

    model_config = ConfigDict(extra="forbid")

    n_configs: int = Field(ge=1)
    select_by: str
    alpha: float = Field(gt=0, lt=1)
    selected: ConfigScore
    default: ConfigScore
    scores: list[ConfigScore]
    recall_bound: CorrectedBound
    precision_bound: CorrectedBound
    default_recall_naive_lower: float = Field(ge=0, le=1)


_SELECT_METRICS = ("balanced_accuracy", "recall", "precision")


def _score_config(
    config: JudgeConfig,
    cases: list[GoldCase],
    rubric: Rubric,
    rules: dict[str, HeuristicCriterionRule],
) -> tuple[ConfigScore, DetectionMetrics]:
    result = evaluate_gold(cases, rubric, rules, predictor=config.predictor)
    m = result.metrics
    return (
        ConfigScore(
            name=config.name,
            tau=config.tau,
            complexity=config.complexity,
            tp=m.tp,
            fp=m.fp,
            fn=m.fn,
            tn=m.tn,
            recall=m.recall.value,
            precision=m.precision.value,
            balanced_accuracy=m.balanced_accuracy,
        ),
        m,
    )


def _corrected_bound(
    metric: str, numerator: int, denominator: int, *, alpha: float, m: int
) -> CorrectedBound:
    est = ProportionEstimate.from_counts(numerator, denominator)
    naive = wilson_lower_bound(numerator, denominator, alpha=alpha, n_comparisons=1)
    corrected = wilson_lower_bound(
        numerator, denominator, alpha=alpha, n_comparisons=m
    )
    return CorrectedBound(
        metric=metric,
        numerator=numerator,
        denominator=denominator,
        point=est.value,
        naive_lower=naive,
        corrected_lower=corrected,
        delta=max(0.0, naive - corrected),
    )


def select_config_and_correct(
    configs: list[JudgeConfig],
    cases: list[GoldCase],
    rubric: Rubric,
    rules: dict[str, HeuristicCriterionRule],
    *,
    select_by: str = "balanced_accuracy",
    alpha: float = 0.05,
) -> ConfigSelection:
    """Score every config on the gold, select the winner, and report the
    multiplicity-corrected Wilson lower bounds for the SELECTED config.

    Selection maximizes ``select_by`` (ties broken toward the LOWER ``tau`` — the
    more conservative, less complex boundary). The correction's family size ``m``
    is the number of configs evaluated.
    """
    if select_by not in _SELECT_METRICS:
        raise ValueError(f"select_by must be one of {_SELECT_METRICS}; got {select_by!r}")
    if not configs:
        raise ValueError("select_config_and_correct requires at least one config")

    scored: list[tuple[ConfigScore, DetectionMetrics]] = [
        _score_config(c, cases, rubric, rules) for c in configs
    ]
    scores = [s for s, _ in scored]
    metrics_by_name = {s.name: m for s, m in scored}

    def _key(s: ConfigScore) -> tuple[float, int]:
        val = getattr(s, select_by)
        # Maximize the metric; tie-break toward the lower tau (negate for max).
        return (val if val is not None else -1.0, -s.tau)

    selected = max(scores, key=_key)
    default = next((s for s in scores if s.tau == 1), scores[0])

    sel_metrics = metrics_by_name[selected.name]
    m = len(configs)
    recall_bound = _corrected_bound(
        "recall", sel_metrics.tp, sel_metrics.tp + sel_metrics.fn, alpha=alpha, m=m
    )
    precision_bound = _corrected_bound(
        "precision", sel_metrics.tp, sel_metrics.tp + sel_metrics.fp, alpha=alpha, m=m
    )
    default_metrics = metrics_by_name[default.name]
    default_recall_naive = wilson_lower_bound(
        default_metrics.tp,
        default_metrics.tp + default_metrics.fn,
        alpha=alpha,
        n_comparisons=1,
    )
    return ConfigSelection(
        n_configs=m,
        select_by=select_by,
        alpha=alpha,
        selected=selected,
        default=default,
        scores=scores,
        recall_bound=recall_bound,
        precision_bound=precision_bound,
        default_recall_naive_lower=default_recall_naive,
    )


